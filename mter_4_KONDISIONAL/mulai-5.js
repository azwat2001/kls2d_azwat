//contoh 7 kondisional dengan switch case
var buttonPushed = 3;     //diganti dari angka 1 sampai 4
switch(buttonPushed) {
  case 1:   { console.log('hidupkan mobil!'); break; }
  case 2:   { console.log('jalankan mobil!'); break; }
  case 3:   { console.log('parkirkan mobil!'); break; }
  case 4:   { console.log('matikan mobil!'); break; }
  default:  { console.log('aman terkendali'); }}
